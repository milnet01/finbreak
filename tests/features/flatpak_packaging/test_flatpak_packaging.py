# --------------------------------------------------------------------------- #
# INV-3 — pip sources sha256-pinned, offline, single-ABI.
# --------------------------------------------------------------------------- #
def test_INV3a_sources_pinned_and_offline() -> None:
    deps = _require_deps()
    saw_source = False
    for mod in _iter_modules(deps):
        for src in mod["sources"]:
            if not isinstance(src, dict) or src.get("type") not in ("file", "archive"):
                continue
            saw_source = True
            sha = src.get("sha256", "")
            assert re.fullmatch(r"[0-9a-f]{64}", str(sha)), (
                f"source {_source_filename(src)!r} lacks a 64-hex sha256"
            )
        for cmd in mod.get("build-commands", []):
            if "pip3 install" in cmd or "pip install" in cmd:
                assert "--no-index" in cmd, f"pip install without --no-index: {cmd!r}"
    assert saw_source, "no pinned file/archive sources found in python3-deps.yaml"


def test_INV3b_no_build_time_network() -> None:
    deps = _require_deps()
    for mod in _iter_modules(deps):
        build_args = (mod.get("build-options") or {}).get("build-args", [])
        assert "--share=network" not in build_args, (
            f"module {mod.get('name')!r} grants build-time network"
        )


def test_INV3c_single_cpython_abi() -> None:
    deps = _require_deps()
    minors: set[str] = set()
    for mod in _iter_modules(deps):
        for src in mod["sources"]:
            if not isinstance(src, dict):
                continue
            name = _source_filename(src)
            if not name.endswith(".whl"):
                continue
            fields = name[: -len(".whl")].split("-")
            if len(fields) < 5:
                continue
            abitag = fields[-2]  # abitag field — NOT a pytag substring scan
            m = re.fullmatch(r"cp(\d+)", abitag)
            if m:  # genuinely per-minor (cp313); abi3 / none are excluded
                minors.add(m.group(1))
    assert len(minors) <= 1, (
        f"mixed CPython ABIs in python3-deps.yaml: {sorted(minors)}"
    )


# --------------------------------------------------------------------------- #
# INV-9 — flathub.json restricts the build to exactly the closure's arches.
# The pinned wheels are x86_64-only (generate-pip-sources.sh --wheel-arches=x86_64),
# but Flathub's buildbot builds EVERY arch by default — so an aarch64 build would
# `pip install --no-index` an x86_64 wheel and fail. flathub.json's `only-arches`
# must equal the concrete arch set the wheel filenames carry, so the two can never
# drift (widen the wheels ⟹ must widen only-arches, and vice versa).
# --------------------------------------------------------------------------- #
def _closure_arches(deps: Any) -> set[str]:
    """The set of concrete CPU arches the pinned wheels target (pure-Python
    `*-none-any` wheels and the sdist are arch-independent — they contribute none)."""
    arches: set[str] = set()
    for mod in _iter_modules(deps):
        for src in mod["sources"]:
            if not isinstance(src, dict):
                continue
            name = _source_filename(src)
            if not name.endswith(".whl"):
                continue
            platform_tags = name[: -len(".whl")].split("-")[-1]  # trailing platform tag
            for tag in platform_tags.split("."):
                for arch in _KNOWN_ARCHES:
                    if tag.endswith(arch):
                        arches.add(arch)
    return arches


def test_INV9_flathub_json_restricts_to_closure_arches() -> None:
    assert _FLATHUB_JSON.exists(), (
        "packaging/flatpak/flathub.json is missing — without `only-arches` Flathub "
        "builds every arch and the aarch64 build fails on the x86_64-only closure"
    )
    config = json.loads(_FLATHUB_JSON.read_text(encoding="utf-8"))
    only_arches = config.get("only-arches")
    assert isinstance(only_arches, list) and only_arches, (
        "flathub.json needs a non-empty only-arches list"
    )

    deps = _require_deps()
    want = _closure_arches(deps)
    assert want, "no concrete-arch wheels found in python3-deps.yaml"
    assert set(only_arches) == want, (
        f"flathub.json only-arches {sorted(only_arches)} != wheel-closure arches "
        f"{sorted(want)} — restrict Flathub to exactly what the pinned wheels cover"
    )


# --------------------------------------------------------------------------- #
# INV-4 — identity + version single-source (new Flatpak-side legs only).
# --------------------------------------------------------------------------- #
def test_INV4_identity_and_version() -> None:
    m = _load(_MANIFEST)
    assert m["id"] == APP_ID
    assert _MANIFEST.name == f"{APP_ID}.yaml", "manifest filename stem must be app-ID"
    # command is the bare console script — NOT the app-ID (§ 3.3).
    assert m["command"] == "finbreak"

    # The finbreak module's git source pins an immutable 40-hex commit (§ 3.8).
    git_commit = None
    for mod in _iter_modules(m):
        if isinstance(mod, dict) and mod.get("name") == "finbreak":
            for src in mod["sources"]:
                if isinstance(src, dict) and src.get("type") == "git":
                    git_commit = src.get("commit")
    assert git_commit is not None, "finbreak module has no git source with a commit"
    assert re.fullmatch(r"[0-9a-f]{40}", str(git_commit)), (
        f"git commit {git_commit!r} is not a 40-hex sha (a tag-only pin fails)"
    )

    # No hand-typed version: no manifest key VALUE is a bare 3-segment semver
    # (runtime-version is 2-segment; the tag carries a leading 'v') — a structural
    # walk, not a raw-text sweep that inline version comments would false-positive.
    for val in _iter_scalars(m):
        assert not re.fullmatch(r"\d+\.\d+\.\d+", str(val)), (
            f"bare semver {val!r} in a manifest value — version must ride the git tag"
        )


