# --------------------------------------------------------------------------- #
# INV-7 — PySide6 pin is the single Qt source.
# --------------------------------------------------------------------------- #
def test_INV7_pyside6_pin_matches_pyproject() -> None:
    deps = _require_deps()
    pyproject = tomllib.loads(_PYPROJECT.read_text(encoding="utf-8"))
    pinned = next(
        d for d in pyproject["project"]["dependencies"] if d.startswith("PySide6==")
    )
    want_version = pinned.split("==", 1)[1]  # 6.11.1

    found = None
    for mod in _iter_modules(deps):
        for src in mod["sources"]:
            if not isinstance(src, dict):
                continue
            name = _source_filename(src)
            # Wheel: PySide6-6.11.1-cp313-...whl (case-insensitive dist name).
            m = re.match(r"(?i)pyside6-([0-9][^-]*)-", name)
            if m:
                found = m.group(1)
    assert found is not None, "no PySide6 wheel found in python3-deps.yaml"
    assert found == want_version, (
        f"PySide6 wheel {found} != pyproject pin {want_version} (Qt source drifted)"
    )


# --------------------------------------------------------------------------- #
# INV-8 — window-centering disabled under Flatpak (the definite src/ change).
# --------------------------------------------------------------------------- #
def test_INV8_centering_disabled_under_flatpak(monkeypatch: pytest.MonkeyPatch) -> None:
    from finbreak.ui import main_window

    # Under Flatpak + KDE-Wayland, the org.kde.KWin call is unreachable (§ 3.5), so
    # _kde_wayland() must return False — the single chokepoint _center_supported()
    # and _center_window() both consult — even though the env looks like KDE Wayland.
    monkeypatch.setattr(main_window, "_in_flatpak", lambda: True)
    monkeypatch.setattr(main_window, "_is_wayland", lambda: True)
    monkeypatch.setenv("XDG_CURRENT_DESKTOP", "KDE")

    assert main_window._kde_wayland() is False, "Flatpak gate must disable KWin path"
    assert main_window._center_supported() is False, (
        "Center window must be disabled under Flatpak (no dead click)"
    )


def test_INV8_gate_is_flatpak_specific(monkeypatch: pytest.MonkeyPatch) -> None:
    # Guard the guard: WITHOUT the Flatpak marker, KDE Wayland still supports
    # centering (the gate is Flatpak-specific, not a blanket disable).
    from finbreak.ui import main_window

    monkeypatch.setattr(main_window, "_in_flatpak", lambda: False)
    monkeypatch.setattr(main_window, "_is_wayland", lambda: True)
    monkeypatch.setenv("XDG_CURRENT_DESKTOP", "KDE")

    assert main_window._kde_wayland() is True
    assert main_window._center_supported() is True


