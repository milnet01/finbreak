# Concrete CPU arches a manylinux platform tag can name (the ones Flathub builds).
# A wheel platform tag is `<arch>` for the pure-Python `any` case (skipped) or ends
# in one of these for a native manylinux wheel.
_KNOWN_ARCHES = ("x86_64", "aarch64", "armv7l", "i686", "ppc64le", "s390x")

# INV-2 — the ENUMERATED sandbox allowlist (§ 3.4). Every finish-args entry must be
# one of these; anything else — a --share=network, any --filesystem=, any
# --talk-name= — fails the test. The coverage boundary is explicit, not a blocklist.
_FINISH_ARGS_ALLOWLIST = frozenset(
    {
        "--socket=wayland",
        "--socket=fallback-x11",
        "--share=ipc",
        "--device=dri",
    }
)


# --------------------------------------------------------------------------- #
# YAML helpers — structural walks (INV-1..4/7 are key-anchored, not substring).
# --------------------------------------------------------------------------- #
def _load(path: Path) -> Any:
    return yaml.safe_load(path.read_text(encoding="utf-8"))


def _iter_scalars(obj: Any):
    """Every scalar (str/int/float) leaf in a nested dict/list — for the INV-1
    KDE/BaseApp-absence and INV-4 semver checks over parsed VALUES (comments are
    stripped by the YAML parser, so a comment mention never false-positives)."""
    if isinstance(obj, dict):
        for v in obj.values():
            yield from _iter_scalars(v)
    elif isinstance(obj, list):
        for v in obj:
            yield from _iter_scalars(v)
    else:
        yield obj


def _iter_modules(obj: Any):
    """Every module-like dict (one carrying a ``sources`` key) anywhere in a
    manifest/deps structure — recursing whatever shape flatpak-pip-generator emits
    (a flat single module, or nested ``modules[].sources[]`` per package, § 3.6)."""
    if isinstance(obj, dict):
        if "sources" in obj:
            yield obj
        for v in obj.values():
            yield from _iter_modules(v)
    elif isinstance(obj, list):
        for v in obj:
            yield from _iter_modules(v)


def _source_filename(src: dict) -> str:
    """The wheel/sdist basename for a pip source — from an explicit filename field
    or the tail of the pinned URL."""
    for key in ("filename", "dest-filename"):
        if isinstance(src.get(key), str):
            return src[key]
    url = src.get("url", "")
    return url.rsplit("/", 1)[-1] if isinstance(url, str) else ""


def _require_deps() -> Any:
    if not _DEPS.exists():
        pytest.skip("python3-deps.yaml not generated yet (generate-pip-sources.sh)")
    return _load(_DEPS)


# --------------------------------------------------------------------------- #
# INV-1 — freedesktop runtime, pinned branch, no distro/KDE Qt.
# --------------------------------------------------------------------------- #
def test_INV1_freedesktop_runtime_pinned() -> None:
    m = _load(_MANIFEST)
    assert m["runtime"] == "org.freedesktop.Platform"
    assert m["sdk"] == "org.freedesktop.Sdk"
    # A REAL pin — two-segment branch, not master/beta/absent.
    assert re.fullmatch(r"\d+\.\d+", str(m["runtime-version"])), (
        f"runtime-version {m['runtime-version']!r} is not a pinned N.N branch"
    )
    # No KDE runtime / PySide BaseApp anywhere in the parsed values (§ 3.1/§ 7).
    for val in _iter_scalars(m):
        if isinstance(val, str):
            assert "org.kde." not in val, f"KDE runtime reference in manifest: {val!r}"
            assert "BaseApp" not in val, (
                f"PySide BaseApp reference in manifest: {val!r}"
            )


